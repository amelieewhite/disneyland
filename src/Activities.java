public class Activities
  {
    private String activityName;
    private String desc;

    public Activities(String n, String d)
    {
      activityName = n;
      desc = d;
    }

    //getters
    public String getName()
    {
      return activityName;
    }
    public String getDesc()
    {
      return desc;
    }
    
  }
